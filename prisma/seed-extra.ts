async function main() {
  console.log(`Seeding ${events.length} extra events…`);

  for (const e of events) {
    const evt = await prisma.event.upsert({
      where: { slug: e.slug },
      update: {
        officialUrl: e.officialUrl ?? null,
        // imageUrl is intentionally NOT updated here — images set via admin are never overwritten by seed
      },
      create: {
        slug: e.slug,
        title: e.title,
        type: e.type,
        status: e.status,
        region: e.region,
        county: e.county ?? null,
        city: e.city ?? null,
        venueName: e.venueName ?? null,
        description: e.description,
        startDate: e.startDate,
        endDate: e.endDate ?? null,
        startTime: e.startTime ?? null,
        endTime: e.endTime ?? null,
        allDay: e.allDay,
        recurringAnnual: e.recurringAnnual,
        free: e.free ?? null,
        indoorOutdoor: e.indoorOutdoor ?? null,
        lat: e.lat ?? null,
        lng: e.lng ?? null,
        routeDescription: e.routeDescription ?? null,
        nearestStation: e.nearestStation ?? null,
        travelNotes: e.travelNotes ?? null,
        tags: e.tags,
        featured: e.featured,
        officialUrl: e.officialUrl ?? null,
        imageUrl: e.imageUrl ?? null,
        postcode: POSTCODES[e.slug] ?? null,
        archived: false,
        photoMeta: {
          create: {
            photoPotentialScore: e.photoScore,
            bestViewingPosition: e.bestViewingPosition ?? null,
            lensSuggestion: e.lensSuggestion ?? null,
            bestArrivalTime: e.bestArrivalTime ?? null,
            daytimeNightValue: e.daytimeNightValue ?? null,
            costumeCeremonial: e.costumeCeremonial ?? false,
            fireSmokeLight: e.fireSmokeLight ?? false,
            bannersFlagsCandles: e.bannersFlagsCandles ?? false,
            processionElements: e.processionElements ?? false,
            ritualSignificance: e.ritualSignificance ?? false,
            protestVisibility: e.protestVisibility ?? false,
            routeMoving: e.routeMoving ?? false,
            safetyNotes: e.safetyNotes ?? null,
            previousYearNotes: e.previousYearNotes ?? null,
            crowdSizeEstimate: e.crowdSizeEstimate ?? null,
          },
        },
        sources: {
          create: {
            sourceType: SourceType.MANUAL,
            notes: "Manually curated",
          },
        },
      },
    });

    // Update crowdSizeEstimate on existing photoMeta records
    if (e.crowdSizeEstimate) {
      await prisma.photoMeta.updateMany({
        where: { eventId: evt.id },
        data: { crowdSizeEstimate: e.crowdSizeEstimate },
      });
    }

    // Set imageUrl only if not already set (never overwrite manually-curated images)
    if (e.imageUrl) {
      await prisma.event.updateMany({
        where: { slug: e.slug, imageUrl: null },
        data: { imageUrl: e.imageUrl },
      });
    }

    // Set postcode only if not already set
    const pc = POSTCODES[e.slug];
    if (pc) {
      await prisma.event.updateMany({
        where: { slug: e.slug, postcode: null },
        data: { postcode: pc },
      });
    }
  }

  // ── Post-loop corrections for existing events ───────────────────────────────

  // Update Corpus Christi London route to include Ukrainian Catholic Cathedral
  await prisma.event.updateMany({
    where: { slug: "london-corpus-christi-procession-2026", routeDescription: null },
    data: {
      routeDescription: "Church of the Immaculate Conception, Farm Street → Grosvenor Square → Duke Street (passing the Ukrainian Catholic Cathedral) → Oxford Street → St James's, Spanish Place, Marylebone",
    },
  });

  // Eid in the Square is the Mayor of London's Eid al-Adha event, confirmed 30 May 2026
  await prisma.event.updateMany({
    where: { slug: "eid-in-the-square-2026" },
    data: {
      startDate: new Date("2026-05-30T12:00:00Z"),
      status: "SCHEDULED",
      officialUrl: "https://www.london.gov.uk/events/eid-square-2026",
    },
  });

  console.log("Done.");
}

